#include "Camera.h"
#include "Car.h"

Camera::Camera ():target(0)
{
}
//--------------------------------------------------------------------------------------------------
Camera::~Camera ()
{
}
//--------------------------------------------------------------------------------------------------
void Camera::Move_right()
{
	pos.x += 10;
}
//--------------------------------------------------------------------------------------------------
void Camera::Move_left()
{
	pos.x -= 10;
}
//--------------------------------------------------------------------------------------------------
void Camera::Move_up()
{
	pos.y -= 10;
}
//--------------------------------------------------------------------------------------------------
void Camera::Move_down()
{
	pos.y += 10;
}
//--------------------------------------------------------------------------------------------------
Point Camera::Get_pos() const
{
	return pos;
}
//--------------------------------------------------------------------------------------------------
void Camera::Actions()
{
	if (!target) return;

	pos += (target->Get_pos().To<int>() - Point (300, 300) - pos)/50;
}
